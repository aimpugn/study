     * 테스트 34 〉	실패 (15.04ms, 65.9MB)
     * 테스트 35 〉	실패 (11.70ms, 65.6MB)
     * 테스트 36 〉	실패 (19.08ms, 65MB)
     * 테스트 37 〉	통과 (16.23ms, 65.6MB)
     * 테스트 38 〉	통과 (0.02ms, 64.8MB)
     * 테스트 39 〉	통과 (0.02ms, 63.2MB)
     * ```
     *
     * [[2, 1], [2, 1], [3, 2]], n=4, m=3 의 경우
     * B 도둑이 [[2, 1], [2, 1]]을 훔쳐가고
     * A 도둑이 [[3, 2]]를 훔쳐서 "3"을 리턴해야 하는데,
     * greedy 방식으로는 "4"를 리턴하게 되기 때문입니다.
     *
     * 그렇다면 처음에 생각을 잘못했고, 그리디 방식이 아닌 조합 최적화가 필요한 것으로 보입니다.
     */
    fun greedy(info: Array<IntArray>, n: Int, m: Int): Int {
        // 우선 배열을 어떻게 정리하고 탐색할지 생각합니다.
        // A 도둑이 남긴 누적 흔적의 최솟값을 구하면 되므로, 훔치는 순서는 중요하지 않을 거 같습니다.
        //
        // [
        //   A  B
        //  [1, 2]
        //  [2, 3]
        //  [2, 1]
        // ]
        //
        // 일단 A의 최솟값을 구해야 하므로, B가 많이 훔치는 방향으로 생각합니다.
        // 우선 B가 훔칠 수 있는 만큼 훔치고 남은 것을 A가 훔칠 수 있는지 확인합니다.
        // 이때 B가 훔치는 순서는 A에게 부담이 큰 것들을 우선 훔칩니다.
        info.sortByDescending { it[0] }

        // 물건 i를 훔쳤는지 기록합니다.
        val stolen = BooleanArray(info.size)

        var aStoleCnt = 0
        var bStoleCnt = 0
        // B 도둑이 m 이하로 훔치는지 확인합니다.
        // knapsack 문제처럼 더하는 순서가 중요하지 않으므로 그냥 순회를 하며 순차적으로 가능한 만큼 다 더합니다.
        for (i in info.indices) {
            val maybe = info[i][1]
            if (bStoleCnt + maybe < m) {
                bStoleCnt += maybe
                stolen[i] = true
            }
        }

        for (i in info.size - 1 downTo 0) {
            if (stolen[i]) {
                continue
            }
            val maybe = info[i][0]

            if (aStoleCnt + maybe < n) {
                aStoleCnt += maybe
                stolen[i] = true
            }
        }

        if (stolen.all { it }) {
            return aStoleCnt
        }

        return -1
    }
}

